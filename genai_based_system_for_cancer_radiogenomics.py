def visualize_results(volume,mask,slice_index):

    ct_slice=volume[slice_index]

    ct_norm=(ct_slice-ct_slice.min())/(ct_slice.max()-ct_slice.min())

    heatmap=gaussian_filter(mask[slice_index].astype(float),sigma=20)

    heatmap=(heatmap-heatmap.min())/(heatmap.max()-heatmap.min())

    overlay=np.stack([ct_norm]*3,axis=-1)

    overlay[:,:,0]+=heatmap*0.8

    overlay=np.clip(overlay,0,1)

    plt.figure(figsize=(18,5))

    # CT
    plt.subplot(1,4,1)
    plt.imshow(ct_slice,cmap="gray")
    plt.title("CT Scan")
    plt.axis("off")

    # Radiologist annotation
    plt.subplot(1,4,2)
    plt.imshow(ct_slice,cmap="gray")
    plt.imshow(mask[slice_index],cmap="Reds",alpha=0.4)
    plt.title("Radiologist Annotation")
    plt.axis("off")

    # AI attention
    plt.subplot(1,4,3)
    plt.imshow(heatmap,cmap="jet")
    plt.title("AI Attention Map")
    plt.axis("off")

    # overlay
    plt.subplot(1,4,4)
    plt.imshow(overlay)
    plt.title("AI Overlay")
    plt.axis("off")

    plt.tight_layout()
    plt.show()


