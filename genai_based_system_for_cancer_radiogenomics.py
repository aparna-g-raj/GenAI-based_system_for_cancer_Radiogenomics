# LOAD CT SCAN
# ================================
def load_ct(patient_id):

    patient_folder=os.path.join(DICOM_DIR,patient_id)

    if not os.path.exists(patient_folder):
        raise ValueError("Patient folder not found")

    series_folder=None
    max_slices=0

    for root,dirs,files in os.walk(patient_folder):

        dicom_files=[f for f in files if f.lower().endswith(".dcm")]

        if len(dicom_files)>max_slices:

            max_slices=len(dicom_files)
            series_folder=root

    if series_folder is None:
        raise ValueError("No DICOM files found")

    print("Using DICOM folder:",series_folder)

    reader=sitk.ImageSeriesReader()

    dicom_names=reader.GetGDCMSeriesFileNames(series_folder)

    reader.SetFileNames(dicom_names)

    image=reader.Execute()

    volume=sitk.GetArrayFromImage(image)

    print("CT Volume Shape:",volume.shape)

    return image,volume


