    # plot
    fig, ax = plt.subplots(1,3, figsize=(12,4))

    ax[0].imshow(slice_img, cmap="gray")
    ax[0].set_title("CT Slice")

    ax[1].imshow(grayscale_cam, cmap="jet")
    ax[1].set_title("Grad-CAM Heatmap")

    ax[2].imshow(overlay)
    ax[2].set_title("Overlay")

    for a in ax:
        a.axis("off")

