def fetch_aim_details(patient_id):
    """
    Extracts key AIM annotation details for a given patient ID
    and returns them in a structured dictionary.
    """

    aim_path = os.path.join(AIM_DIR, f"{patient_id}.xml")

    if not os.path.exists(aim_path):
        return None

    tree = ET.parse(aim_path)
    root = tree.getroot()

    # AIM XML namespace
    ns = {'aim': 'gme://caCORE.caCORE/4.4/edu.northwestern.radiology.AIM'}

    # -----------------------------
    # Basic Patient Information
    # -----------------------------
    details = {
        "Patient ID": patient_id,
        "Gender": "Unknown",
        "Lesion Name": "NA",
        "Observations": []
    }

    # Gender
    sex = root.find('.//aim:person/aim:sex', ns)
    if sex is not None:
        details["Gender"] = sex.get('value')

    # Lesion Name
    lesion = root.find('.//aim:ImageAnnotation/aim:name', ns)
    if lesion is not None:
        details["Lesion Name"] = lesion.get('value')

    # Imaging Observations (AIM Labels)
    observations = root.findall('.//aim:ImagingObservationCharacteristic', ns)
    for obs in observations:
        label = obs.find('aim:label', ns)
        if label is not None:
            details["Observations"].append(label.get('value'))

    return details

