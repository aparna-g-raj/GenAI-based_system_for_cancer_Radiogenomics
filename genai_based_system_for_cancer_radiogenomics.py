# READ AIM ANNOTATION
# ================================
def read_aim_circle(aim_file):

    tree=ET.parse(aim_file)
    root=tree.getroot()

    coords=[]
    slice_index=None

    for elem in root.iter():

        tag=elem.tag.lower()

        if "referencedframenumber" in tag:

            slice_index=int(elem.attrib["value"])

        if "twodimensionspatialcoordinate" in tag:

            x=None
            y=None

            for child in elem:

                child_tag=child.tag.lower()

                if child_tag.endswith("x"):
                    x=float(child.attrib["value"])

                if child_tag.endswith("y"):
                    y=float(child.attrib["value"])

            if x is not None and y is not None:

                coords.append((x,y))

    return slice_index,coords


# ================================
# CREATE TUMOR MASK
# ================================
def create_mask(volume_shape,slice_index,coords):

    mask=np.zeros(volume_shape,dtype=np.uint8)

    slice_index=min(slice_index,volume_shape[0]-1)

    x1,y1=coords[0]
    x2,y2=coords[1]

    center_x=int(x1)
    center_y=int(y1)

    radius=int(np.sqrt((x2-x1)**2+(y2-y1)**2))*6

    Y,X=np.ogrid[:volume_shape[1],:volume_shape[2]]

    circle=(X-center_x)**2+(Y-center_y)**2<=radius**2

    mask[slice_index][circle]=1

    return mask


