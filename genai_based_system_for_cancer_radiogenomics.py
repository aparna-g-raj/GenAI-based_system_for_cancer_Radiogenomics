def gradcam_for_patient(image_path):

    image = sitk.ReadImage(image_path)
    volume = sitk.GetArrayFromImage(image)

    # choose middle slice
    slice_index = volume.shape[0] // 2
    slice_img = volume[slice_index]

    input_tensor = torch.tensor(slice_img).unsqueeze(0).unsqueeze(0).float().to(DEVICE)

    mask = np.ones((slice_img.shape[0], slice_img.shape[1]))
    targets = [SemanticSegmentationTarget(0, mask)]

    grayscale_cam = cam(input_tensor=input_tensor, targets=targets)[0]

    # normalize image
    image_norm = (slice_img - slice_img.min()) / (slice_img.max() - slice_img.min() + 1e-8)
    image_rgb = np.stack([image_norm, image_norm, image_norm], axis=-1)

