# CREATE TUMOR MASK (ENLARGED)
# ================================
def create_mask(volume_shape, slice_index, coords):

    mask = np.zeros(volume_shape, dtype=np.uint8)

    slice_index = min(slice_index, volume_shape[0]-1)

    x1,y1 = coords[0]
    x2,y2 = coords[1]

    center_x = int(x1)
    center_y = int(y1)

    radius = int(np.sqrt((x2-x1)**2 + (y2-y1)**2))*6

    Y,X = np.ogrid[:volume_shape[1],:volume_shape[2]]

    circle = (X-center_x)**2 + (Y-center_y)**2 <= radius**2

    mask[slice_index][circle] = 1

    return mask


