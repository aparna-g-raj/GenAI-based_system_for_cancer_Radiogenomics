def explain_genomics_for_patient(patient_id,X_genes,explainer):

    genomic_id=patient_id.replace("AMC","R01")

    if genomic_id not in X_genes.index:

        print("\nRNA-seq genomic data not available")

        return

    patient_data=X_genes.loc[[genomic_id]]

    shap_values=explainer.shap_values(patient_data)

    shap_df=pd.DataFrame({

        "Gene":patient_data.columns,
        "Influence":shap_values[0]

    })

    shap_df["Impact"]=abs(shap_df["Influence"])

    top_genes=shap_df.sort_values("Impact",ascending=False).head(10)

    print("\nTop genes influencing AI prediction:\n")

    print(top_genes[["Gene","Influence"]])

    # ============================================================
    # BIOLOGICAL PATHWAY VALIDATION (PER PATIENT)
    # ============================================================

    print("\n--- Biological Pathway Validation ---")

    # Define biologically meaningful pathways (fixed, not changing)
    pathways = {

    # Signaling pathways
    "RTK Signaling": ["ERBB","EGFR","FGFR","MET"],
    "PI3K/AKT Pathway": ["PIK","AKT","MTOR"],
    "MAPK Pathway": ["MAPK","MAP"],

    # Cell growth & proliferation
    "Cell Proliferation": ["MYC","FOS","JUN"],
    "Cell Cycle": ["CDK","CCN","E2F"],

    # Apoptosis
    "Apoptosis": ["BAX","BCL","CASP","TP53"],

    # DNA & replication
    "DNA Replication": ["PRIM","MCM","PCNA"],
    "DNA Repair": ["BRCA","ATM","ATR"],

    # Structural / invasion
    "Extracellular Matrix": ["COL","MMP"],

    # Immune & inflammation
    "Immune Response": ["TNF","IL","CD"],

    # Transcription regulation
    "Transcription Factors": ["ZNF","SCAND","FOXA","SOX"],

    # Protein synthesis
    "Ribosomal Proteins": ["RPS","RPL"]
}

    # Get top genes
    genes = top_genes["Gene"].tolist()

    pathway_hits = {}
    total_hits = 0

    for pathway, patterns in pathways.items():
        matched = [g for g in genes if any(p in g for p in patterns)]

        if matched:
            pathway_hits[pathway] = matched
            total_hits += len(matched)

    # Print results
    if pathway_hits:
        for p, g in pathway_hits.items():
            print(f"{p}: {g}")
    else:
        print("No strong pathway signals detected")

    # Score
    pathway_score = total_hits / len(genes) if len(genes) > 0 else 0

    print("\nPathway Biological Score:", round(pathway_score, 3))


