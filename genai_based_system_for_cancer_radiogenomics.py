# CREATE 3D TUMOR MASK
# ================================
def create_tumor_mask_3d(volume_shape, slice_index, coords, thickness=4):

    z, h, w = volume_shape
    mask = np.zeros(volume_shape, dtype=np.uint8)

    slice_index = min(slice_index, z - 1)

    x1, y1 = coords[0]
    x2, y2 = coords[1]

    center_x = int(np.clip(x1, 0, w-1))
    center_y = int(np.clip(y1, 0, h-1))

    radius = int(np.sqrt((x2-x1)**2 + (y2-y1)**2))

    Y, X = np.ogrid[:h, :w]
    circle = (X-center_x)**2 + (Y-center_y)**2 <= radius**2

    for dz in range(-thickness, thickness+1):

        z_slice = slice_index + dz

        if 0 <= z_slice < z:
            mask[z_slice][circle] = 1

    return mask


