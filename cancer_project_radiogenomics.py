import pydicom
import numpy as np
import SimpleITK as sitk

def dicom_series_to_sitk(dicom_dir):
    # list dcm files
    files = [os.path.join(dicom_dir,f) for f in os.listdir(dicom_dir) if f.lower().endswith('.dcm')]
    if not files:
        # sometimes files are in nested subfolders. walk deeper:
        for root,_,fnames in os.walk(dicom_dir):
            for f in fnames:
                if f.lower().endswith('.dcm'):
                    files.append(os.path.join(root,f))
    if not files:
        raise FileNotFoundError(f"No DICOM files found in {dicom_dir}")

    # read headers
    slices = [pydicom.dcmread(f, force=True) for f in files]
    # filter only slices that have InstanceNumber / ImagePositionPatient
    slices = [s for s in slices if hasattr(s,'InstanceNumber') or hasattr(s,'ImagePositionPatient')]
    # try sort by InstanceNumber then fallback to ImagePositionPatient z
    try:
        slices = sorted(slices, key=lambda s: int(s.InstanceNumber))
    except Exception:
        slices = sorted(slices, key=lambda s: float(s.ImagePositionPatient[2]))

    img3d = np.stack([s.pixel_array for s in slices], axis=0)
    # spacing
    px = slices[0].PixelSpacing  # [row spacing, col spacing]
    try:
        slice_thickness = float(slices[0].SliceThickness)
    except Exception:
        # estimate from adjacent slices
        if len(slices) > 1:
            z0 = slices[0].ImagePositionPatient[2]
            z1 = slices[1].ImagePositionPatient[2]
            slice_thickness = abs(z1 - z0)
        else:
            slice_thickness = 1.0

    # Convert to SimpleITK image (note: SITK expects z,y,x order when GetImageFromArray)
    sitk_img = sitk.GetImageFromArray(img3d)  # z,y,x
    sitk_img.SetSpacing((float(px[0]), float(px[1]), float(slice_thickness)))
    return sitk_img

