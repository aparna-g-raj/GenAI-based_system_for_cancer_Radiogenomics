# -----------------------------
# Helper Functions
# -----------------------------
def compute_max_diameter(mask):
    """
    Memory-safe approximation of maximum 3D tumor diameter (mm)
    using bounding-box corners.
    """
    arr = sitk.GetArrayFromImage(mask)
    coords = np.argwhere(arr > 0)
    if len(coords) == 0:
        return 0.0

    spacing = np.array(mask.GetSpacing())

    # bounding box corners
    min_c = coords.min(axis=0)
    max_c = coords.max(axis=0)

    # physical distance
    diff = (max_c - min_c) * spacing
    return np.linalg.norm(diff)


def compute_shape_features(mask):
    """Compute basic shape features from mask."""
    arr = sitk.GetArrayFromImage(mask)
    voxel_volume = np.prod(mask.GetSpacing())
    coords = np.argwhere(arr > 0)
    if len(coords) == 0:
        return {'Volume':0, 'BBox_X':0, 'BBox_Y':0, 'BBox_Z':0, 'Max3DDiameter':0}
    volume = np.sum(arr>0) * voxel_volume
    min_c = coords.min(axis=0)
    max_c = coords.max(axis=0)
    bbox = max_c - min_c
    max3d = compute_max_diameter(mask)  # true largest distance
    return {'Volume':volume, 'BBox_X':bbox[2], 'BBox_Y':bbox[1], 'BBox_Z':bbox[0], 'Max3DDiameter':max3d}

def compute_intensity_features(image, mask):
    """Compute intensity statistics for the tumor ROI."""
    arr = sitk.GetArrayFromImage(image)
    mask_arr = sitk.GetArrayFromImage(mask)
    roi = arr[mask_arr>0]
    if len(roi) == 0:
        return {'Mean':0,'Median':0,'Std':0,'Skewness':0}
    return {
        'Mean': np.mean(roi),
        'Median': np.median(roi),
        'Std': np.std(roi),
        'Skewness': skew(roi)
    }

