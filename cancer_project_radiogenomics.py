"""**U-Net segmentation**"""

import os
import torch
import numpy as np
import matplotlib.pyplot as plt
import SimpleITK as sitk
import segmentation_models_pytorch as smp
from torch.utils.data import Dataset, DataLoader


# ==============================
# Dataset class
# ==============================

class NSCLCDataset(Dataset):

    def __init__(self, image_dir):
        self.image_dir = image_dir

        self.image_files = [
            f for f in os.listdir(image_dir)
            if f.endswith("_preprocessed.nii.gz")
        ]

    def __len__(self):
        return len(self.image_files)

    def __getitem__(self, idx):

        img_path = os.path.join(self.image_dir, self.image_files[idx])

        image = sitk.ReadImage(img_path)
        img_array = sitk.GetArrayFromImage(image)

        return img_array, self.image_files[idx]


# ==============================
# Load model
# ==============================

DEVICE = "cuda" if torch.cuda.is_available() else "cpu"

model = smp.Unet(
    encoder_name="resnet34",
    encoder_weights="imagenet",
    in_channels=1,
    classes=1,
)

model.to(DEVICE)
model.eval()


# ==============================
# Directories
# ==============================

PREPROC_DIR = "/content/drive/MyDrive/Radiogenomics_Dataset/Preprocessed_Images/"
SEGMENTED_DIR = "/content/drive/MyDrive/Radiogenomics_Dataset/Segmented_Images_Unet/"

os.makedirs(SEGMENTED_DIR, exist_ok=True)


# ==============================
# Data loader
# ==============================

dataset = NSCLCDataset(PREPROC_DIR)

loader = DataLoader(
    dataset,
    batch_size=1,
    shuffle=False
)

print("Total patients:", len(dataset))


# ==============================
# Segmentation loop
# ==============================

for img_arrays, filenames in loader:

    img_arrays = img_arrays[0].numpy()

    preds_array = np.zeros_like(img_arrays)

    patient_id = filenames[0].split("_preprocessed")[0]

    Z,H,W = img_arrays.shape

    print("Processing:", patient_id)


    for z in range(Z):

        img_slice = img_arrays[z,:,:]

        img_tensor = torch.tensor(
            img_slice,
            dtype=torch.float32
        ).unsqueeze(0).unsqueeze(0).to(DEVICE)


        with torch.no_grad():

            pred = model(img_tensor)

            pred = torch.sigmoid(pred)

            pred = (pred > 0.5).float()


        preds_array[z,:,:] = pred.cpu().numpy()[0,0]


    # Save segmentation
    out_mask = sitk.GetImageFromArray(preds_array)

    out_path = os.path.join(
        SEGMENTED_DIR,
        f"{patient_id}_unet_mask.nii.gz"
    )

    sitk.WriteImage(out_mask, out_path)

    print("Saved mask:", out_path)


    # ==============================
    # Tri-planar visualization
    # ==============================

    mid_z = Z//2
    mid_y = H//2
    mid_x = W//2

    fig,axes = plt.subplots(1,3, figsize=(15,5))

    axes[0].imshow(img_arrays[mid_z,:,:], cmap="gray")
    axes[0].imshow(preds_array[mid_z,:,:], cmap="Reds", alpha=0.4)
