def dicom_series_to_sitk(series_folder):
    """Load DICOM series into SITK image."""
    try:
        reader = sitk.ImageSeriesReader()
        dicom_names = reader.GetGDCMSeriesFileNames(series_folder)
        reader.SetFileNames(dicom_names)
        image = reader.Execute()
        return image
    except Exception as e:
        print(f" Failed to load {series_folder}: {e}")
        return None

def resample_image(image, new_spacing=[1,1,1]):
    """Resample to uniform voxel spacing."""
    original_spacing = image.GetSpacing()
    original_size = image.GetSize()
    new_size = [int(round(original_size[i] * (original_spacing[i] / new_spacing[i]))) for i in range(3)]
    resampler = sitk.ResampleImageFilter()
    resampler.SetOutputSpacing(new_spacing)
    resampler.SetSize(new_size)
    resampler.SetInterpolator(sitk.sitkLinear)
    resampler.SetOutputDirection(image.GetDirection())
    resampler.SetOutputOrigin(image.GetOrigin())
    return resampler.Execute(image)

def normalize_intensity(image, min_val=-1000, max_val=400):
    """Clip and scale intensity to 0-1 for DL."""
    arr = sitk.GetArrayFromImage(image)
    arr = np.clip(arr, min_val, max_val)
    arr = (arr - min_val) / (max_val - min_val)
    return sitk.GetImageFromArray(arr)

def crop_or_pad(image, target_size=[128,128,128]):
    """Crop/pad to fixed size (for DL)."""
    arr = sitk.GetArrayFromImage(image)
    z, y, x = arr.shape
    target_z, target_y, target_x = target_size
    pad_z = max(0, target_z - z)
    pad_y = max(0, target_y - y)
    pad_x = max(0, target_x - x)
    arr = np.pad(arr,
                 ((pad_z//2, pad_z - pad_z//2),
                  (pad_y//2, pad_y - pad_y//2),
                  (pad_x//2, pad_x - pad_x//2)),
                 mode='constant', constant_values=0)
    arr = arr[:target_z, :target_y, :target_x]
    return sitk.GetImageFromArray(arr)

def view_image(image, slice_index=None, vmin=-1000, vmax=400):
    """Visualize a middle slice (axial) of the image with proper CT windowing."""
    arr = sitk.GetArrayFromImage(image)  # (Z,Y,X)
    z = arr.shape[0]
    if slice_index is None:
        slice_index = z // 2  # middle slice
    plt.imshow(arr[slice_index,:,:], cmap='gray', vmin=vmin, vmax=vmax)
    plt.title(f"Slice {slice_index}")
    plt.axis('off')
    plt.show()

